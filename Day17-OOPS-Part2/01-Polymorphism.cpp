/*
    Polymorphism:
    - Polymorphism is a feature of OOP that allows us to perform a single action in different ways.
    - It allows us to use the same name to refer to different classes with similar functionality.
    - Poly means many and morphism means forms, so polymorphism means many forms.
    - Polymorphism is a Greek word that means "many-shaped" and it has two types:
        1. Compile Time Polymorphism
        2. Run Time Polymorphism
    - Compile Time Polymorphism:
        This occurs when the function signature (function name, return type, and parameter list) is the same in different derived classes but the implementation is different.
    - Run Time Polymorphism:
        This occurs when the function signature is the same in different derived classes and the implementation is also the same.
    - Polymorphism is achieved by using function overloading and function overriding.
    - Function Overloading:
        Function overloading is a feature that allows us to have more than one function with the same name but with different parameters.
    - Function Overriding:
        Function overriding is a feature that allows us to have a function in the derived class with the same name and signature as a function in the base class.
*/